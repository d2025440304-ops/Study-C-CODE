// 结构体是一种特殊的类型，和int,double,float,char没有本质的区别；

 struct peo//就相当于int
{
    int age ;
    char name [12];
    char tele [12];
};

struct ebook
{
    struct peo data[100];//可以存放100个人
    int count;//记录已经存放的个数
};

int main()
{
    struct peo p1 = {18,"wnager","2025440304"};
    struct ebook eb = {{{18,"yiyi","1538466919"},{17,"dada","556552"}},0};
    printf("%s\n",p1.name);
    printf("%s\n",eb.data[1].name);
    printf("%s\n",eb.data[0].name);
    return 0;
}